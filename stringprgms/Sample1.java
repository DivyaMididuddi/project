package stringprgms;

public class Sample1 
{
	public static void main(String[] args) 
	{
		//length:int
		String s1="html";
		int i=s1.length();
		System.out.println(i);
		//charAt():char
		String s2="developer";
		char ch=s2.charAt(6);
		System.out.println(ch);
		
	}

}
//length is used to count no.of characters where as charAt gives the character at a place even index number is given                                                                                                           